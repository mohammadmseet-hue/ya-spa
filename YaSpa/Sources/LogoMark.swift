import SwiftUI

/// Ya Spa brand-mark colors — sugar off-white ground, grass-green jasmine,
/// deep botanical-green arch, antique-gold accents. The mark itself is shipped
/// as a bundled asset ("SplashLogo") so it renders as a rock-solid image rather
/// than live-drawn shapes.
enum Logo {
    static let sugar  = Color(hex: 0xF7F3E9)   // sugar off-white background
    static let grass  = Color(hex: 0x7BA23F)   // grass green
    static let forest = Color(hex: 0x2E4B34)   // deep green
    static let gold   = Color(hex: 0xC9A15E)   // antique gold
}
